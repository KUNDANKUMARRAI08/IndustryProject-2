public class variabledemo {

    public static void main(String args[]) {
        int a = 10;
        System.out.println(a);

        float v = 10.2f;
        System.out.println(v);

        double var = 10.256985;
        System.out.println(var);

        char c = 'a';
        System.out.println(c);

        a = 12;
        System.out.println(a);

        boolean b = false;
        System.out.println(b);

    }

}
